package Collections;

public class Coordinates {
    private int x; //Максимальное значение поля: 985
    private long y;

    public Coordinates(int x, long y) {
        this.x = x;
        this.y = y;
    }


    public int getX() {
        return x;
    }

    public long getY() {
        return y;
    }


}